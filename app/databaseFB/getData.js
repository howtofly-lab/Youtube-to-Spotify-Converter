import {doc, getDoc } from "firebase/firestore";
import { db } from '../firebase';
export default async function getData(collection, id) {
    let docRef = doc(db, collection, id);

    let result = null;
    let error = null;

    try {
        result = await getDoc(docRef);
    } catch (e) {
        error = e;
    }

    return { result, error };
}
